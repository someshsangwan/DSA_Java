public class Intro {
    // 1. Stack
    // Definition:
    // A stack is a linear data structure that follows the LIFO (Last In, First Out)
    // principle.
    // The last element added to the stack is the first one to be removed.
    // Operations:
    // Push: Add an element to the top of the stack.
    // Pop: Remove the top element from the stack.
    // Peek/Top: Retrieve the top element without removing it.
    // isEmpty: Check if the stack is empty.
    // Applications:
    // Expression evaluation (e.g., postfix, prefix).
    // Expression conversion (e.g., infix to postfix).
    // Backtracking (e.g., solving mazes, recursion).
    // Undo/Redo operations in text editors.
    // Function call stack in programming.
    // Implementation:
    // Can be implemented using:
    // Array (fixed size).
    // Linked List (dynamic size).
    // Time Complexity:
    // Push, Pop, Peek: O(1) (constant time).
}
